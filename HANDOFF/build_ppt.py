def set_text(shape, text):
    tf = shape.text_frame
    lines = str(text).split("\n")
    p0 = tf.paragraphs[0]
    if p0.runs:
        p0.runs[0].text = lines[0]
        for r in p0.runs[1:]:
            r._r.getparent().remove(r._r)
    else:
        p0.text = lines[0]
    for extra in list(tf.paragraphs[1:]):
        extra._p.getparent().remove(extra._p)
    for line in lines[1:]:
        np_ = tf.add_paragraph()
        run = np_.add_run()
        run.text = line
        if p0.runs:
            src = p0.runs[0].font
            run.font.size = src.size
            run.font.bold = src.bold
            try:
                if src.color and src.color.rgb:
                    run.font.color.rgb = src.color.rgb
            except Exception:
                pass


def S(i):
    return prs.slides[i].shapes


def gget(shapes, path):
    sh = shapes[path[0]]
    for idx in path[1:]:
        sh = sh.shapes[idx]
    return sh


def setp(slide_idx, path, text):
    try:
        set_text(gget(S(slide_idx), path), text)
    except Exception as e:
        print(f"  ! slide{slide_idx} path{path}: {e}")


def dup(src_idx):
    src = prs.slides[src_idx]
    new = prs.slides.add_slide(src.slide_layout)
    for sh in list(new.shapes):
        sh._element.getparent().remove(sh._element)
    for sh in src.shapes:
        new.shapes._spTree.append(copy.deepcopy(sh._element))
    return len(prs.slides) - 1


